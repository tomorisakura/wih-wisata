package grevi.msx.wihwisata;

public class Wisata {
    private int picWisata;
    private int harga_wisata;
    private String nama_wisata;
    private String Lokasi_wisata;
    private String kategory_wisata;
    private String Info_wisata;

    public int getPicWisata() {
        return picWisata;
    }

    public void setPicWisata(int picWisata) {
        this.picWisata = picWisata;
    }

    public int getHarga_wisata() {
        return harga_wisata;
    }

    public void setHarga_wisata(int harga_wisata) {
        this.harga_wisata = harga_wisata;
    }

    public String getNama_wisata() {
        return nama_wisata;
    }

    public void setNama_wisata(String nama_wisata) {
        this.nama_wisata = nama_wisata;
    }

    public String getLokasi_wisata() {
        return Lokasi_wisata;
    }

    public void setLokasi_wisata(String lokasi_wisata) {
        Lokasi_wisata = lokasi_wisata;
    }

    public String getKategory_wisata() {
        return kategory_wisata;
    }

    public void setKategory_wisata(String kategory_wisata) {
        this.kategory_wisata = kategory_wisata;
    }

    public String getInfo_wisata() {
        return Info_wisata;
    }

    public void setInfo_wisata(String info_wisata) {
        Info_wisata = info_wisata;
    }
}
